a = int(input('Введіть вартість однієї ігрової приставки:'))
b = int(input('Введіть Їхню кількість:'))
c = int(input('Введіть відсоток знижки:'))
print('1 - Порахувати загальнру суму замовлення')
print('2 - Порахувати вартість однієї приставки з урахуванням знижки')
choice = input('Оберіть дію: ')
if choice == '1':
    print('Загальна сума:',(a-(a/100*c))*b)
elif choice == '2':
    print('вартість однієї приставки з урахуванням знижки:',a-(a/100*c))
else:
    print('Невірна дія')