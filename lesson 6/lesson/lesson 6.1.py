a = int(input('Введіть кількість секунд:'))
print('1 - Порахувати години до півночі')
print('2 - Порахувати хвилини до півночі')
print('3 - Порахувати секунди до півночі')
choice = input('Оберіть дію: ')
if choice == '1':
    print('До півночі ',24-(a/1440), 'годин')
elif choice == '2':
    print('До півночі ',1440-(a/60), 'хвилин')
elif choice == '3':
    print('До півночі ',86400-a, 'секунд')
else:
    print('Невірна дія')